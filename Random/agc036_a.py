S = int(input())

# 三角形の3つの頂点の座標を求める
# 頂点1: (0, 0) に固定
x1, y1 = 0, 0

# 頂点2: (10^9, 1) に固定
x2, y2 = 10**9, 1

# 頂点3: (x3, y3) を求める
# 三角形の面積 = |x2*y3 - x3*y2| / 2 = S / 2
# |10^9 * y3 - x3| / 2 = S / 2
# |10^9 * y3 - x3| = S
# 10^9 * y3 - x3 = S として解く

# S を 10^9 で割った商と余りを求める
quotient = S // (10**9)  # y3
remainder = S % (10**9)  # x3

# ただし、x3 を 0 以上 10^9 未満にする必要がある
# 模範回答の方法: x3 = (10^9 - remainder) % 10^9
x3 = (10**9 - remainder) % (10**9)
y3 = quotient

print(x1, y1, x2, y2, x3, y3)
